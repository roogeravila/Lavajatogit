
package lavajato;

public class celula {
    double hora;
    String funçao;
    
    public celula(double hora, String funçao) {
        this.hora = hora;
        this.funçao = funçao;
    }

    public double getHora() {
        return hora;
    }

    public String getFunçao() {
        return funçao;
    }
    
}
